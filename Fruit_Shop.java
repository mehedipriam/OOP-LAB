        int banana = Integer.parseInt(jTextField1.getText());
        int mango = Integer.parseInt(jTextField2.getText());
        int apple = Integer.parseInt(jTextField3.getText());
        
        int p_banana = banana*5;
        int p_mango = mango*10;
        int p_apple = apple*15;
        
        int sum = p_banana + p_mango + p_apple;
        
        JOptionPane.showMessageDialog(null, "Total : "+sum ,"Shop",JOptionPane.INFORMATION_MESSAGE);
        
