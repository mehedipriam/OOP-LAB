            FileWriter file = new FileWriter("Output",true);
            PrintWriter out = new PrintWriter(file);
            
            out.println("Banana : "+jTextField1.getText());
            out.println("Mango : "+jTextField2.getText());
            out.println("Apple : "+jTextField3.getText());
            out.println("Total : "+sum);
            out.println("---------------------------");
            out.close();

      
